function updateStep() {
  root.dataset.step = String(currentStep);
  if (stepLabel) {
    stepLabel.textContent = `ORDER ${String(Math.min(currentStep + 1, 3)).padStart(2, "0")}`;
  }
  orderCards.forEach((card, index) => {
    card.classList.toggle("is-active", index === currentStep && !finished[index]);
    card.classList.toggle("is-done", finished[index]);
  });
  cups.forEach((cup, index) => {
    cup.classList.toggle("is-active", index === currentStep && !finished[index]);
    cup.classList.toggle("is-filled", finished[index]);
  });
}

function updateTray() {
  if (!loadedTray) {
    return;
  }
  loadedTray.innerHTML = "";
  loadedIngredients.forEach((name) => {
    const chip = document.createElement("span");
    chip.className = "loaded-chip";
    chip.textContent = ingredientNames[name]?.[currentLanguage] || name;
    loadedTray.appendChild(chip);
  });
  ingredients.forEach((button) => {
    button.classList.toggle("is-used", loadedIngredients.includes(button.dataset.ingredient));
  });
  if (loadedIngredients.length === 0) {
    setStatus("statusReady");
    setLiquidLevel(0);
    root.style.setProperty("--juice", "transparent");
    root.style.setProperty("--juice-glow", "transparent");
  } else {
    setStatus("statusLoaded");
    setLiquidLevel(18 + loadedIngredients.length * 14);
  }
}

function clearLoaded() {
  if (isBlending || isPouring) {
    return;
  }
  loadedIngredients = [];
  jarObjects = [];
  ingredientWorld.innerHTML = "";
  targetLiquidColor = [18, 20, 28];
  liquidMix = 0;
  updateTray();
}

function addIngredient(name) {
  if (finished.every(Boolean) || isBlending || isPouring || !name || loadedIngredients.includes(name)) {
    return;
  }
  if (loadedIngredients.length >= 2) {
    clearLoaded();
  }
  loadedIngredients.push(name);
  createJarObject(name);
  mixTowardIngredientColor(name);
  pulseAt(window.innerWidth * 0.5, window.innerHeight * 0.46, 12);
  playTone(160 + loadedIngredients.length * 80);
  updateTray();
}

function sameCombo(a, b) {
  return a.length === b.length && a.every((item) => b.includes(item));
}

function setLiquidLevel(value) {
  targetLiquidLevel = Math.max(0, Math.min(82, Number(value) || 0));
  root.style.setProperty("--liquid-height", `${targetLiquidLevel}%`);
}

function mixTowardIngredientColor(name) {
  const skin = objectSkins[name];
  if (!skin) {
    return;
  }
  targetLiquidColor = skin.rgb;
  liquidMix = Math.max(liquidMix, 0.18);
  root.style.setProperty("--juice-glow", skin.glow);
}

function averageColor(names) {
  if (names.length === 0) {
    return [18, 20, 28];
  }
  const total = names.reduce(
    (sum, name) => {
      const rgb = objectSkins[name]?.rgb || [120, 130, 150];
      return [sum[0] + rgb[0], sum[1] + rgb[1], sum[2] + rgb[2]];
    },
    [0, 0, 0],
  );
  return total.map((value) => Math.round(value / names.length));
}

function createJarObject(name) {
  if (!ingredientWorld || !machineJar) {
    return;
  }
  const skin = objectSkins[name] || objectSkins.chrome;
  const element = document.createElement("span");
  const rect = machineJar.getBoundingClientRect();
  const size = Math.max(34, Math.min(58, rect.width * 0.105));
  const object = {
    id: objectId += 1,
    name,
    element,
    x: rect.width * (0.36 + Math.random() * 0.28),
    y: 18,
    entryY: -rect.height * (0.14 + Math.random() * 0.18),
    z: -20 + Math.random() * 44,
    vx: -1.2 + Math.random() * 2.4,
    vy: 0.6 + Math.random() * 0.9,
    vz: -0.2 + Math.random() * 0.4,
    rx: Math.random() * 70,
    ry: Math.random() * 80,
    rz: Math.random() * 360,
    spinX: -2 + Math.random() * 4,
    spinY: -3 + Math.random() * 6,
    spinZ: -4 + Math.random() * 8,
    size,
    scale: 1,
    opacity: 1,
    orbit: Math.random() * Math.PI * 2,
    radius: rect.width * (0.18 + Math.random() * 0.12),
  };
  element.className = "jar-object";
  element.dataset.kind = name;
  element.style.setProperty("--object-radius", skin.radius || "46%");
  element.style.setProperty("--object-fill", skin.fill);
  element.style.setProperty("--object-glow", skin.glow);
  element.style.setProperty("--size", `${size}px`);
  ingredientWorld.appendChild(element);
  jarObjects.push(object);
  object.y = object.entryY;
  renderJarObject(object);
}

function renderJarObject(object) {
  object.element.style.setProperty("--x", `${object.x - object.size / 2}px`);
  object.element.style.setProperty("--y", `${object.y - object.size / 2}px`);
  object.element.style.setProperty("--z", `${object.z}px`);
  object.element.style.setProperty("--rx", `${object.rx}deg`);
  object.element.style.setProperty("--ry", `${object.ry}deg`);
  object.element.style.setProperty("--rz", `${object.rz}deg`);
  object.element.style.setProperty("--scale", object.scale.toFixed(3));
  object.element.style.setProperty("--opacity", object.opacity.toFixed(3));
}

function updateJarObjects() {
  if (!machineJar) {
    return;
  }
  const rect = machineJar.getBoundingClientRect();
  const floor = rect.height * 0.84;
  const left = rect.width * 0.1;
  const right = rect.width * 0.9;
  const centerX = rect.width * 0.5;
  const centerY = rect.height * 0.58;

  jarObjects.forEach((object) => {
    if (isPouring) {
      object.y += 1.8;
      object.scale *= 0.96;
      object.opacity *= 0.94;
      object.rx += object.spinX * 2;
      object.ry += object.spinY * 2;
      object.rz += object.spinZ * 2;
      return;
    }

    if (isBlending) {
      object.orbit += 0.16 + object.id * 0.006;
      object.radius *= 0.975;
      object.x = centerX + Math.cos(object.orbit) * object.radius;
      object.y = centerY + Math.sin(object.orbit) * object.radius * 0.42;
      object.z = Math.sin(object.orbit * 1.6) * 70;
      object.scale *= 0.982;
      object.opacity *= 0.978;
      object.rx += object.spinX * 3.4;
      object.ry += object.spinY * 3.4;
      object.rz += object.spinZ * 3.4;
      liquidMix = Math.min(1, liquidMix + 0.015);
      return;
    }

    object.vy += 0.18;
    object.x += object.vx;
    object.y += object.vy;
    object.z += object.vz;
    object.rx += object.spinX;
    object.ry += object.spinY;
    object.rz += object.spinZ;

    if (object.x < left || object.x > right) {
      object.vx *= -0.72;
      object.x = Math.max(left, Math.min(right, object.x));
    }
    if (object.y > floor - object.size * 0.5) {
      object.y = floor - object.size * 0.5;
      object.vy *= -0.52;
      object.vx *= 0.9;
      object.spinZ *= 0.88;
    }
  });

  jarObjects = jarObjects.filter((object) => {
    const alive = object.opacity > 0.04 && object.scale > 0.08;
    if (!alive) {
      object.element.remove();
    }
    return alive;
  });
  jarObjects.forEach(renderJarObject);
}

function resizeBlendCanvas() {
  if (!blendCanvas || !machineJar) {
    return;
  }
  const rect = machineJar.getBoundingClientRect();
  const ratio = Math.min(window.devicePixelRatio || 1, 2);
  blendCanvas.width = Math.max(1, Math.floor(rect.width * ratio));
  blendCanvas.height = Math.max(1, Math.floor(rect.height * ratio));
  blendCanvas.style.width = `${rect.width}px`;
  blendCanvas.style.height = `${rect.height}px`;
  blendCtx.setTransform(ratio, 0, 0, ratio, 0, 0);
  const count = rect.width < 360 ? 110 : 170;
  liquidParticles = Array.from({ length: count }, (_, index) => {
    const depth = Math.random();
    return {
      phase: (index / count) * Math.PI * 2 + Math.random() * 0.4,
      radius: rect.width * (0.08 + Math.random() * 0.36),
      y: Math.random(),
      depth,
      size: 1.4 + depth * 4.2 + Math.random() * 1.8,
      speed: 0.006 + Math.random() * 0.018,
      alpha: 0.16 + depth * 0.34,
    };
  });
}

function drawBlendCanvas() {
  if (!blendCanvas || !machineJar) {
    return;
  }
  const rect = machineJar.getBoundingClientRect();
  blendCtx.clearRect(0, 0, rect.width, rect.height);
  visualLiquidLevel += (targetLiquidLevel - visualLiquidLevel) * 0.08;
  liquidColor = liquidColor.map((value, index) =>
    value + (targetLiquidColor[index] - value) * (isBlending ? 0.045 : 0.025),
  );

  const liquidHeight = rect.height * (visualLiquidLevel / 100);
  if (liquidHeight > 2) {
    const baseY = rect.height - rect.height * 0.07 - liquidHeight;
    const color = liquidColor.map((value) => Math.round(value));
    const floorY = rect.height * 0.93;
    const centerX = rect.width * 0.5;
    const centerY = baseY + liquidHeight * 0.5;

    blendCtx.save();
    blendCtx.beginPath();
    blendCtx.moveTo(rect.width * 0.1, floorY);
    for (let index = 0; index <= 36; index += 1) {
      const progress = index / 36;
      const x = rect.width * (0.1 + progress * 0.8);
      const wave = Math.sin(progress * Math.PI * 4 + blendFrame * 0.13) * (isBlending ? 10 : 3);
      const y = baseY + wave;
      blendCtx.lineTo(x, y);
    }
    blendCtx.lineTo(rect.width * 0.9, floorY);
    blendCtx.closePath();
    blendCtx.clip();

    blendCtx.globalCompositeOperation = "lighter";
    liquidParticles.forEach((particle) => {
      if (isBlending) {
        particle.phase += particle.speed * 4.8;
        particle.radius *= 0.999;
      } else {
        particle.phase += particle.speed;
      }
      const orbitScale = 0.28 + particle.depth * 0.5;
      const wobble = Math.sin(blendFrame * 0.025 + particle.phase * 2) * (isBlending ? 8 : 2);
      const x = centerX + Math.cos(particle.phase) * particle.radius;
      const y =
        baseY +
        liquidHeight * (0.12 + particle.y * 0.78) +
        Math.sin(particle.phase) * rect.height * 0.08 * orbitScale +
        wobble;
      const perspective = 0.58 + particle.depth * 0.72;
      const size = particle.size * perspective * (isBlending ? 1.08 : 1);
      const alpha = Math.min(0.78, particle.alpha + liquidMix * 0.18);

      const bead = blendCtx.createRadialGradient(x - size * 0.28, y - size * 0.34, 0, x, y, size * 2.4);
      bead.addColorStop(0, `rgba(255, 255, 255, ${0.7 * alpha})`);
      bead.addColorStop(0.28, `rgba(${color[0]}, ${color[1]}, ${color[2]}, ${0.42 * alpha})`);
      bead.addColorStop(0.7, `rgba(${color[0]}, ${color[1]}, ${color[2]}, ${0.18 * alpha})`);
      bead.addColorStop(1, "rgba(255, 255, 255, 0)");
      blendCtx.fillStyle = bead;
      blendCtx.beginPath();
      blendCtx.ellipse(x, y, size * 1.45, size * 0.92, particle.phase, 0, Math.PI * 2);
      blendCtx.fill();
    });

    const surface = blendCtx.createLinearGradient(0, baseY - 18, 0, baseY + 28);
    surface.addColorStop(0, `rgba(255, 255, 255, ${0.24 + liquidMix * 0.12})`);
    surface.addColorStop(0.45, `rgba(${color[0]}, ${color[1]}, ${color[2]}, ${0.16 + liquidMix * 0.12})`);
    surface.addColorStop(1, "rgba(255, 255, 255, 0)");
    blendCtx.globalCompositeOperation = "screen";
    blendCtx.fillStyle = surface;
    blendCtx.beginPath();
    blendCtx.ellipse(centerX, baseY + (isBlending ? Math.sin(blendFrame * 0.08) * 4 : 0), rect.width * 0.35, 18, 0, 0, Math.PI * 2);
    blendCtx.fill();

    if (isBlending) {
      for (let ring = 0; ring < 5; ring += 1) {
        const radius = 30 + ring * 24 + Math.sin(blendFrame * 0.08 + ring) * 7;
        blendCtx.strokeStyle = `rgba(255, 255, 255, ${0.2 - ring * 0.025})`;
        blendCtx.lineWidth = 1.1;
        blendCtx.beginPath();
        blendCtx.ellipse(
          centerX,
          centerY,
          radius * 1.55,
          radius * 0.42,
          blendFrame * 0.035 + ring,
          0,
          Math.PI * 2,
        );
        blendCtx.stroke();
      }
    }
    blendCtx.restore();
  }
  blendFrame += 1;
}

function wrongRecipe() {
  root.classList.add("is-error");
  setStatus("statusWrong");
  root.style.setProperty("--juice", "linear-gradient(120deg, #19191f, #ff304f, #09090c)");
  root.style.setProperty("--juice-glow", "rgba(255, 48, 79, 0.7)");
  targetLiquidColor = [255, 48, 79];
  liquidMix = 0.4;
  setLiquidLevel(48);
  pulseAt(window.innerWidth * 0.5, window.innerHeight * 0.44, 34);
  playTone(96);
  window.setTimeout(() => {
    root.classList.remove("is-error");
    clearLoaded();
  }, 760);
}

function completeStep(recipe) {
  const cup = cups[currentStep];
  finished[currentStep] = true;
  root.style.setProperty("--juice", recipe.color);
  root.style.setProperty("--juice-glow", recipe.glow);
  targetLiquidColor = recipe.rgb;
  liquidMix = 1;
  if (cup) {
    cup.style.setProperty("--cup-color", recipe.color);
    cup.style.setProperty("--cup-glow", recipe.glow);
    cup.style.setProperty("--cup-fill", "0%");
    cup.classList.add("is-filled");
    requestAnimationFrame(() => {
      cup.style.setProperty("--cup-fill", "78%");
    });
  }
  setStatus("statusCorrect");
  pulseAt(window.innerWidth * 0.5, window.innerHeight * 0.44, 42);
  playTone(recipe.tone);
  isPouring = true;
  juicer?.classList.add("is-pouring");
  setLiquidLevel(0);
  window.setTimeout(() => {
    juicer?.classList.remove("is-pouring");
    isPouring = false;
    jarObjects = [];
    ingredientWorld.innerHTML = "";
    loadedIngredients = [];
    if (finished.every(Boolean)) {
      unlockTeaser();
      return;
    }
    currentStep = finished.findIndex((done) => !done);
    setLiquidLevel(0);
    updateTray();
    updateStep();
  }, 1320);
}

function blend() {
  if (isBlending || isPouring) {
    return;
  }
  if (loadedIngredients.length !== 2) {
    setStatus("statusNeedTwo");
    playTone(128);
    return;
  }
  const recipe = recipes[currentStep];
  juicer?.classList.add("is-blending");
  isBlending = true;
  blendFrame = 0;
  targetLiquidColor = recipe.rgb;
  liquidMix = 0;
  setLiquidLevel(82);
  window.setTimeout(() => {
    juicer?.classList.remove("is-blending");
    isBlending = false;
    if (sameCombo(loadedIngredients, recipe.ingredients)) {
      completeStep(recipe);
    } else {
      wrongRecipe();
    }
  }, 1800);
}

