let lang = localStorage.getItem("neorigin-language") || "zh";
let width = 0;
let height = 0;
let dpr = 1;
let frame = 0;
let pulse = 0;
let themeIndex = 0;
const themes = ["ice", "silver", "blueprint"];

function t(key) {
  return translations[lang]?.[key] || translations.zh[key] || "";
}

function applyLanguage(nextLang) {
  lang = translations[nextLang] ? nextLang : "zh";
  document.documentElement.lang = lang === "zh" ? "zh-CN" : lang === "ko" ? "ko" : "en";
  i18nNodes.forEach((node) => {
    const value = t(node.dataset.i18n);
    if (value) node.textContent = value;
  });
  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });
  if (tagOutput) tagOutput.textContent = t("tagOutput");
  localStorage.setItem("neorigin-language", lang);
}

function showToast(message) {
  if (!toast) return;
  toast.textContent = message;
  toast.classList.add("is-visible");
  window.clearTimeout(showToast.timer);
  showToast.timer = window.setTimeout(() => toast.classList.remove("is-visible"), 1600);
}

function resizeCanvas() {
  width = window.innerWidth;
  height = window.innerHeight;
  dpr = Math.min(window.devicePixelRatio || 1, width < 720 ? 1.35 : 1.7);
  canvas.width = Math.floor(width * dpr);
  canvas.height = Math.floor(height * dpr);
  canvas.style.width = `${width}px`;
  canvas.style.height = `${height}px`;
  ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
}

function drawSignal() {
  frame += 1;
  pulse *= 0.94;
  ctx.clearRect(0, 0, width, height);
  ctx.globalAlpha = 0.64;
  const count = width < 620 ? 28 : 42;
  for (let i = 0; i < count; i += 1) {
    const seed = i * 19.91;
    const x = (Math.sin(frame * 0.006 + seed) * 0.5 + 0.5) * width;
    const y = (Math.cos(frame * 0.004 + seed * 0.7) * 0.5 + 0.5) * height;
    const size = 10 + ((i * 11) % 34) + pulse * 36;
    ctx.save();
    ctx.translate(x, y);
    ctx.rotate(Math.sin(frame * 0.011 + seed) * 0.5);
    ctx.strokeStyle =
      i % 3 === 0 ? "rgba(158,188,204,.34)" :
      i % 3 === 1 ? "rgba(106,223,255,.42)" :
      "rgba(16,24,32,.18)";
    ctx.lineWidth = 1;
    ctx.strokeRect(-size * 0.5, -size * 0.5, size, size);
    ctx.restore();
  }
  ctx.globalAlpha = 1;
  requestAnimationFrame(drawSignal);
}

function activateTheme() {
  themeIndex = (themeIndex + 1) % themes.length;
  body.dataset.theme = themes[themeIndex];
  pulse = 1;
  showToast(["ICE SIGNAL", "SILVER MODE", "BLUEPRINT READY"][themeIndex]);
}

function makeDraggable(element) {
  const bar = element.querySelector(".window-bar") || element;
  let pointerId = null;
  let startX = 0;
  let startY = 0;
  let baseX = 0;
  let baseY = 0;

  bar.addEventListener("pointerdown", (event) => {
    if (window.matchMedia("(max-width: 760px)").matches) return;
    pointerId = event.pointerId;
    const rect = element.getBoundingClientRect();
    startX = event.clientX;
    startY = event.clientY;
    baseX = rect.left;
    baseY = rect.top;
    element.classList.add("is-dragging");
    try {
      element.setPointerCapture(pointerId);
    } catch {
      pointerId = null;
      element.classList.remove("is-dragging");
    }
  });

  element.addEventListener("pointermove", (event) => {
    if (pointerId !== event.pointerId) return;
    const nextX = Math.max(12, Math.min(window.innerWidth - element.offsetWidth - 12, baseX + event.clientX - startX));
    const nextY = Math.max(86, Math.min(window.innerHeight - element.offsetHeight - 12, baseY + event.clientY - startY));
    element.style.left = `${nextX}px`;
    element.style.top = `${nextY}px`;
    element.style.right = "auto";
    element.style.bottom = "auto";
  });

  function finishDrag(event) {
    if (pointerId !== event.pointerId) return;
    if (element.hasPointerCapture?.(pointerId)) {
      element.releasePointerCapture(pointerId);
    }
    pointerId = null;
    element.classList.remove("is-dragging");
  }

  element.addEventListener("pointerup", finishDrag);
  element.addEventListener("pointercancel", finishDrag);
}

function resetWindowPositionsForMobile() {
  if (!window.matchMedia("(max-width: 760px)").matches) return;
  windows.forEach((item) => {
    item.style.left = "";
    item.style.top = "";
    item.style.right = "";
    item.style.bottom = "";
  });
}

function renderSocialLinks() {
  if (!socialList || socialLinks.length === 0) return;
  socialList.hidden = false;
  socialList.innerHTML = socialLinks
    .map(
      (link) =>
        `<a href="${link.url}" target="_blank" rel="noopener noreferrer" aria-label="${link.name}"><img class="social-icon-img" src="https://cdn.simpleicons.org/${link.icon}/111111?viewbox=auto" alt="" aria-hidden="true" /><span class="social-label">${link.name}</span></a>`,
    )
    .join("");
}

function protectMedia() {
  document.querySelectorAll("img, .protected-media").forEach((item) => {
    item.setAttribute("draggable", "false");
    item.addEventListener("dragstart", (event) => event.preventDefault());
    item.addEventListener("contextmenu", (event) => event.preventDefault());
  });
}

